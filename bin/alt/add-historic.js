function addHistoric(geodata){

    var geo=JSON.parse(geodata);
    
 

    for (let i=0;i<geo.features.length;i++){
	
	let properties=geo.features[i].properties;
	
        let id=properties.id;
	let tags=properties.tags;
	
	if(historic[id]){
	    hist=historic[id];
	    //process.stderr.write(JSON.stringify(hist,null,2));
            for(let i=0;i<hist.length;i++){
		h=hist[i];
		let key='circumference:historic:'+h.jahr
		let value=h.umfang;
		//process.stderr.write(key+'='+value);
		tags[key]=value
	    }
	}
    }
    process.stdout.write(JSON.stringify(geo,null,2)+'\n');
	    
}


